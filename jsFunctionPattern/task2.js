// Оголошення рекурсивної функції

function recursiveCounter(num) {
	// 1. Виводимо поточне число в консоль
	console.log(num);

	// 2. Умова виходу: якщо num стає менше або рівне 0, зупиняємося
	if (num <= 0) {
		return;
	}

	// 3. Рекурсивний виклик зі зменшеним значенням і зменшуємо num на 1
	recursiveCounter(num - 1);
}

// Виклик функції
recursiveCounter(5); // Виведе: 5, 4, 3, 2, 1, 0
